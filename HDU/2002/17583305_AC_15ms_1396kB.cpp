#include<bits/stdc++.h>
using namespace std;
int main()
{
    double pi=3.1415927;
    double r;
    while(scanf("%lf",&r)!=EOF)
    {
        printf("%.3f\n",double(4)/double(3)*pi*r*r*r);
    }
}
